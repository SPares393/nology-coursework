switch (new Date().getDay()) {
  case 0:
    console.log("It's already the weekend!");
    break;
  case 1:
    console.log("4 days left");
    break;
  case 2:
    console.log("3 days left");
    break;
  case 3:
    console.log("2 days left");
    break;
  case 4:
    console.log("1 days left");
    break;
  case 5:
    console.log("Last day!");
    break;
  case 6:
    console.log("It's already the weekend!");
    break;
}
